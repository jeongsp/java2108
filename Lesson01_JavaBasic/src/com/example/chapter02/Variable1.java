package com.example.chapter02;

public class Variable1 {

	public static void main(String[] args) {
		// 여기부터 코딩 시작
		// 변수란? 데이터를 보관하는 기억장소의 이름
		// 변수 선언 구문
		// 타입 변수명;
		int level; // int는 정수형, level은 변수명
		int age;
			// assignment operater (대입연산자)
		// 변수 level에 정수형 숫자 값 10을 대입하여라(보관하여라)
		level = 10; 
		age = 49;
		// System 클래스의 out을 이용하여 println함수를 이용하여 level을 출력하여라
		System.out.println(level);
		System.out.println(age);

	}// end of main()

}// end of class Variable1
